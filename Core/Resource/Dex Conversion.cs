                    found = true;
                }
            }

            return value;
        }

        public int Gen3GetDexNum(int dex)
        {
            int value = dex;
            bool found = false;
            if (value > 251)
            {
                for(int i = 252; i < 387 && found == false; i++)
                {
                    if (gen3NatNums[i] == value)
                    {
                        value = i;
                        found = true;
                    }
                    else if (i + 1 == 387)
                        value = 387;
                }
            }
            return value;
        }

